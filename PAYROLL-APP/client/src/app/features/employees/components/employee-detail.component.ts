  onIdNumberChange() {
    const id = this.emp.id_number?.trim();
    if (!id || id.length !== 13 || !/^\d{13}$/.test(id)) return;
    const yy = parseInt(id.substring(0, 2), 10);
    const mm = parseInt(id.substring(2, 4), 10);
    const dd = parseInt(id.substring(4, 6), 10);
    const year = yy >= 0 && yy <= 30 ? 2000 + yy : 1900 + yy;
    if (mm >= 1 && mm <= 12 && dd >= 1 && dd <= 31) {
      this.emp.date_of_birth = `${year}-${String(mm).padStart(2, '0')}-${String(dd).padStart(2, '0')}`;
    }
    const genderDigits = parseInt(id.substring(6, 10), 10);
    this.emp.gender = genderDigits >= 5000 ? 'Male' : 'Female';
    this.cdr.detectChanges();
  }

  get idValid(): boolean { return this.emp.id_number && /^\d{13}$/.test(this.emp.id_number.trim()); }
  get dobLocked(): boolean { return this.idValid; }

  get calculatedAge(): number | null {
    if (!this.emp.date_of_birth) return null;
    const dob = new Date(this.emp.date_of_birth);
    const today = new Date();
    let age = today.getFullYear() - dob.getFullYear();
    const m = today.getMonth() - dob.getMonth();
    if (m < 0 || (m === 0 && today.getDate() < dob.getDate())) age--;
    return age;
  }

