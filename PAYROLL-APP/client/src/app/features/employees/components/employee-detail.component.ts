  buildMergedList(): void {
    const empHeadIds = new Set(this.employeeTxns.map((t: any) => t.salary_head_id));
    const inherited = (this.inheritedGroup?.items || [])
      .filter((item: any) => !empHeadIds.has(item.salary_head_id))
      .map((item: any) => ({
        ...item,
        source: 'inherited',
        amount: null,
        percentage: null,
        included_in_package: item.included_in_package !== false,
        taxable_pct: item.taxable ? 100 : 0
      }));
    const employee = this.employeeTxns.map((t: any) => ({
      id: t.id,
      salary_head_id: t.salary_head_id,
      code: t.salary_head_code,
      name: t.salary_head_name,
      transaction_type: t.transaction_type,
      calculation_method: t.calculation_method,
      irp5_code: t.irp5_code,
      taxable: t.taxable,
      employer_contribution: t.employer_contribution,
      employee_contribution: t.employee_contribution,
      amount: t.amount,
      percentage: t.percentage,
      included_in_package: t.included_in_package !== false,
      start_date: t.start_date,
      end_date: t.end_date,
      source: 'employee',
      taxable_pct: t.taxable ? 100 : 0
    }));
    const typeOrder: Record<string, number> = { EARNING: 0, DEDUCTION: 1, COMPANY_CONTRIBUTION: 2 };
    this.mergedTxns = [...inherited, ...employee].sort((a, b) => {
      const ta = typeOrder[a.transaction_type] ?? 3;
      const tb = typeOrder[b.transaction_type] ?? 3;
      if (ta !== tb) return ta - tb;
      return (a.salary_head_id || 0) - (b.salary_head_id || 0);
    });
  }

  getTypeLabel(t: string): string {
    const m: Record<string, string> = { EARNING: 'Earnings', DEDUCTION: 'Deductions', COMPANY_CONTRIBUTION: 'Company Contributions' };
    return m[t] || t;
  }

  getTypeColor(t: string): string {
    const m: Record<string, string> = { EARNING: '#22c55e', DEDUCTION: '#ef4444', COMPANY_CONTRIBUTION: '#3b82f6' };
    return m[t] || '#6b7280';
  }

  get groupedTxns(): { type: string, label: string, color: string, items: any[] }[] {
    const groups: { type: string, label: string, color: string, items: any[] }[] = [];
    const typeOrder = ['EARNING', 'DEDUCTION', 'COMPANY_CONTRIBUTION'];
    for (const type of typeOrder) {
      const items = this.mergedTxns.filter(t => t.transaction_type === type);
      if (items.length > 0) {
        groups.push({ type, label: this.getTypeLabel(type), color: this.getTypeColor(type), items });
      }
    }
    return groups;
  }

